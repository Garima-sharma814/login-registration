const mongoose = require("mongoose");

//Contacted user sechema
const contactSchema = new mongoose.Schema({
  fullname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
});

const ContactUser = mongoose.model("contact", contactSchema);

module.exports = ContactUser;